# ─── Notion API 建立頁面 ─────────────────────────────────────────────────────
def create_page(token: str, db_id: str, props: dict) -> str:
    """建立一筆 Notion 頁面，回傳頁面 ID。"""
    url = "https://api.notion.com/v1/pages"
    headers = {
        "Authorization":  f"Bearer {token}",
        "Notion-Version": VER,
        "Content-Type":   "application/json",
    }
    payload = {
        "parent": {"database_id": db_id},
        "properties": props,
    }
    req = urllib.request.Request(
        url, data=json.dumps(payload).encode("utf-8"), headers=headers
    )
    try:
        with urllib.request.urlopen(req) as resp:
            data = json.load(resp)
            return data["id"]
    except urllib.error.HTTPError as e:
        body = e.read().decode()
        print(f"  ❌ HTTP {e.code}: {body[:200]}")
        return ""


def build_props(sys_name: str, sub_name: str, item: dict, sort_num: int) -> dict:
    """組裝 Notion properties dict。"""
    sev_label = SEV_LABEL[item["sev"]]
    return {
        "系統":   {"select": {"name": sys_name}},
        "子系統": {"select": {"name": sub_name}},
        "項目ID": {"title": [{"text": {"content": item["id"]}}]},
        "嚴重性": {"select": {"name": sev_label}},
        "核對項目": {"rich_text": [{"text": {"content": item["text"]}}]},
        "法規依據": {"rich_text": [{"text": {"content": item["basis"]}}]},
        "啟用":   {"checkbox": True},
        "排序":   {"number": sort_num},
    }


# ─── 主程式 ─────────────────────────────────────────────────────────────────
def main():
    print(f"🚀 開始寫入 Notion 資料庫: {DB_ID}")
    print(f"   Token: {TOKEN[:20]}...")

    total = 0
    ok    = 0
    sort_base = {
        "弱電系統": 101,
        "給水系統": 201,
        "排水系統": 301,
        "消防系統": 401,
        "空調系統": 501,
    }

    for sys_data in SYSTEMS:
        sys_name = sys_data["系統"]
        base     = sort_base[sys_name]
        print(f"\n【{sys_name}】")

        sort_offset = 0
        for sub_data in sys_data["subs"]:
            sub_name = sub_data["子系統"]
            print(f"  ├─ {sub_name}")

            for item in sub_data["items"]:
                sort_num = base + sort_offset
                sort_offset += 1
                total += 1

                props = build_props(sys_name, sub_name, item, sort_num)
                page_id = create_page(TOKEN, DB_ID, props)

                if page_id:
                    ok += 1
                    print(f"  │  ✅ {item['id']} (排序 {sort_num})")
                else:
                    print(f"  │  ❌ {item['id']} 失敗")

    print(f"\n{'='*50}")
    print(f"完成: {ok}/{total} 筆寫入成功")
    if ok < total:
        print(f"⚠️  {total - ok} 筆失敗，請檢查上方錯誤訊息")
    else:
        print("🎉 全部成功！")


if __name__ == "__main__":
    main()
